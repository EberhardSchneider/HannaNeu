			<p>Hanna Herfurtner ist eine vielseitige Konzert­- und Opernsängerin.</p>

<p>Bereits während des Studiums konnte sie sich ein breites Oratorienrepertoire erarbeiten, welches
	
	von Monteverdi bis Honegger reicht.
	
	Mittlerweile ist sie als <b>Konzertsängerin</b> europaweit zu erleben, unter anderem im <i>Konzerthaus</i> in
	
	<i>Berlin, Wien, Wroclaw</i> und <i>Stavangar</i>, dem <i>Mecklenburg Vorpommern Festival</i>, dem <i>Theater an
		
		der Wien</i> sowie der <i>Oper Oslo</i>. Auch wenn sich in ihrem Konzertkalender eine gewisse Bachdichte
	
	verzeichnen lässt, ist sie doch in den Oratorien Händels und Mozarts ebenso zu Hause wie denen
	
	Mendelssohns und Brahms.</p>

<p>Hanna Herfurtner hat eine große Affinität zur <b>Alten Musik</b>. Sie ist Preisträgerin des ersten <i>Cesti-
		Wettbewerbs</i> für Barockoper in Innsbruck, 2010. Mehrfach war sie <i>Singer in Residence</i> beim
	<i></i>
	Festival für Alte Musik <i>Trigonale</i> in Kärnten, wo sie u.a. mit Franco Pavan, Alfredo Bernardini
	
	und Barokksolistene auftrat. Mit letzteren führte sie 2015 eine Konzertreise mit einer
	
	Rekonstruktion der Bach ́schen Markuspassion an die <i>Oper Oslo</i>, das <i>Konzerthaus Stavangar</i> und
	
	die <i>Domkirche in Bergen</i>. Unter der Leitung von Rubén Dubrowski sang sie die Ninfa in Porporas
	
	„Polifemo“ am <i>Theater an der Wien</i> und die Almirena in Händels „Rinaldo“ in Breslau. Ostern
	
	2016 folgt ein „Messiah“ in Klosterneuburg. Mit dem L'Orfeo Barockorchester unter Michi
	
	Gaigg sang sie 2015 die Arminda in Mozarts „Verstellter Gärtnerin“ bei den <i>Donaufestwochen im
		
		Strudengau</i>.<br>
	
	2014 gab sie ihren ersten Meisterkurs bei der Barockwoche der Musikhochschule Frankfurt am
	
	Main.</p>

<p>Sie ist außerdem eine erfahrene Interpretin <b>Zeitgenössischer Musik</b>. Sie besuchte einige Jahre die
	
	Klasse für zeitgenössisches Lied von Axel Bauni an der UdK Berlin. Sie wirkte bei mehreren
	
	Uraufführungen mit und arbeitete mit so namhaften Komponisten zusammen wie Manfred Trojahn
	
	und Detlev Glanert. 2010 sang sie die Titelpartie in der Uraufführung von Hans Werner Henzes
	
	letzter Oper „Gisela“ bei der <i>Ruhrtriennale</i> (R: Pierre Audi, D: Steven Sloane).</p>

<p>Als <b>Opernsängerin</b> führten sie Engagements zur <i>Ruhrtriennale</i> (Schönberg: „Moses und Aron“),
	
	zu den <i>Bregenzer Festspielen</i> (u.a. Mozart: „Zauberflöte“, „Schauspieldirektor“; Laks: „L
	
	 ́hirondelle inattendue“), der <i>Staatsoper Berlin</i> (Birtwistle: „Punch and Judy“) und der <i>Oper Köln</i>
	
	(Glanert: „Solaris“). 2011 debütierte sie unter Christian Thielemann bei den <i>Salzburger
		
		Festspielen</i> (Strauss: „Frau ohne Schatten). 2012 war sie Ensemblemitglied der <i>Oper Kiel</i>, wo sie
	
	die Rolle der Olympia in „Les contes d ́Hoffmann“ von Offenbach, Fraarte in Händels
	
	„Radamisto“ und Poussette in Massenets „Manon“ sang. Sie wirkte in preisgekrönten
	
	Opernproduktionen mit, u.a. 2011/12 in Wolf­-Ferraris „Aschenputtel“ in der Titelpartie an
	
	der <i>Staatsoper Berlin</i> (R: Eva­-Maria Weiss, Studiopreis Götz Friedrich Stiftung) sowie
	
	2015 als L'Amour in Glucks „Orphée et Eurydice“ an der <i>Nationalen Reisopera</i>,
	
	Niederlande, (R: Floris Visser, Oper des Jahres 2015 des Opernmagazins Place de l'Opera).<br>
	
	2016/17 wird sie als Helmwiege in Wagners „Walküre“ an der <i>Oper Frankfurt</i> debütieren.</p>

<p>2009 gewann sie mit dem Piansten stefanpaul beim <i>Paula­-Salomon­-Lindberg Wettbewerb</i> „das
	
	Lied“ den ersten Preis. Seitdem war sie mit unterschiedlichen Programmen (Wagner und andere
	
	Franzosen; Britten und andere Briten; Mahler und der Wiener Jugendstil, „Mais je rapporte une
	
	rare emotion“ u.a.) beim <i>Heidelberger Frühling</i>, der <i>Oper Kiel</i> und den <i>Musikverein Coburg</i> zu
	
	Gast.
	
	2012 gab sie beim <i>Rheingau Musikfestival</i> zusammen mit Jonathan Ware und Benjamin Appl das
	
	Italienische Liederbuch von Hugo Wolf.
	
	Sie ist außerdem Mitglied des 2015 gegründeten <b>Trio Ardesco</b>.</p>

<p>Sie studierte bei Prof. Bernhard Jäger­Böhm an der Musikhochschule, Stuttgart (2004­2006), bei
	
	Prof. Julie Kaufmann an der UdK, Berlin (2006­2012), und John Norris.</p>



